const rows = input.split('\n');
const rowLength = rows[0].length;
const trees = (colInc, rowInc) => {
    let count = 0;
    let index = 0;
    let rowIndex = 0;

    for (let row of rows) {
        rowIndex++;

        if (rowInc && rowIndex === rowInc) {
            rowIndex = 0;
            continue;
        }

        const pos = (index * colInc) % rowLength;
        index++;

        if (row[pos] === '#') {
            count++;
        }
    }

    return count;
};

